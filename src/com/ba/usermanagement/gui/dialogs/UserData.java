    public void generateUsername() {
        try {
            if (jTextField1.getText().equals("") || jTextField1.getText().equals("")) {
                throw new ValidationException("Ingrese el nombre y apellido del usuario");
            }
            userController.generateUsername(this.user);
            jTextField9.setText(this.user.getUsername());
        } catch (Exception e) {
            GUIMensajes.mostrarErrorReportar(this, e);
        }
    }

    public void save() {
        try {
            userController.saveOrUpdate(user);
            if (this.newUser) {
                setPassword();
            }
            this.dispose();
        } catch (Exception e) {
            GUIMensajes.mostrarErrorReportar(this, e);
        }
    }

    public void cancel() {
        dispose();
    }

    public void setPassword() {
        UserPasswords passwords = new UserPasswords((JFrame) this.getParent(), this.newUser, this.user);
        passwords.setLocationRelativeTo(this);
        passwords.setVisible(true);
    }

